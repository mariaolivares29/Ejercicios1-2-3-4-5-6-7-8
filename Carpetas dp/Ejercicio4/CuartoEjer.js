let digito1 = Number(prompt("Los numeros seran iguales?\nIngrese el numero 1"));
let digito2 = Number(prompt("Ingrese el numero 2"));

if (digito1 == digito2){ 
    document.write("El digito ", digito1, " es igual a el numero ", digito2);
} else {
    document.write("El digito ", digito1, " es diferente a el numero ", digito2);
}